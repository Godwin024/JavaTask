    public static void main(String [] args){
//        Find the sum of numbers from 1 to 100

               

            for(int count = 1; count <= 5; count ++){                  

                for(int counter = 1; counter <= count; counter++){
                    
                    System.out.print(" *");   

            }
                          
              

        System.out.println();
            }


            

    }



